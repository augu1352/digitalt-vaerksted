class Fag:
    def __init__(self, fagKey, fagName):
        self.fagKey = fagKey
        self.fagName = fagName

    def get_fagKey(self):
        return self.fagKey

    def set_fagKey(self, newFagKey):
        self.fagKey = newFagKey

    def get_fagName(self):
        return self.fagName

    def set_fagName(self, newFagName):
        self.fagName = newFagName
